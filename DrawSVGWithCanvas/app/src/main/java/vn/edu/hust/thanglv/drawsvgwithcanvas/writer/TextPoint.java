package vn.edu.hust.thanglv.drawsvgwithcanvas.writer;

public class TextPoint {
    float f35x;
    float f36y;

    public float getX() {
        return this.f35x;
    }

    public void setX(float x) {
        this.f35x = x;
    }

    public float getY() {
        return this.f36y;
    }

    public void setY(float y) {
        this.f36y = y;
    }

    public TextPoint(float x, float y) {
        this.f35x = x;
        this.f36y = y;
    }
}
